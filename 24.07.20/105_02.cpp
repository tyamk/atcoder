#include <bits/stdc++.h>
using namespace std;

// 入力
int main() {
  int a;
  cin >> a; // 変数aで入力を受け取る
  cout << a * 10 << endl;
  string text;
  double d; 
  cin >> text; // 文字列textで入力を受け取る
  cin >> d; // 変数dで入力を受け取る
  cout << text << ", " << d << endl;
  int x, b, c;
  cin >> x >> b >> c;
  cout << x * b * c << endl;
  
}


